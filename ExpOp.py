a = 20
b = 5

# Arithmetic Operators
print(a + b) 
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)

# Comparison Operators
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

# Logical Operators
print(a > 10 and b < 6) 
print(a > 6 or b < 6)
print(not(a > 6 and b < 7))